#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	c = n % 10 + '0';
	if (n / 10 > 0)
	{
		n /= 10;
		ft_putnbr_fd(n, fd);
	}
	write(fd, &c, 1);
}
