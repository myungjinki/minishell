#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (!*lst)
	{
		*lst = new;
		(*lst)->next = 0;
		return ;
	}
	while (*lst)
		lst = &((*lst)->next);
	*lst = new;
}
