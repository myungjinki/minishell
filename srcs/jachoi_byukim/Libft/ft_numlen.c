#include "ft_utils.h"

int		ft_numlen(int64_t n, int base)
{
	int		i;
	int64_t	k;

	i = 1;
	k = base;
	while (k >= base)
	{
		if (-1ll * (k - 1) <= n && n <= (k - 1))
			return (i + (n < 0));
		k *= base;
		i++;
	}
	return (i + (n < 0));
}
